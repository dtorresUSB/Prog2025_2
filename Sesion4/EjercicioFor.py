for i in range(5):      #un parametro indica: el final
    print(i)            # i se llama "indice"

print('--------------------------------------------------------------')

for j in range(5,10):      #dos parametros indican: inicio y fin
    print(j)               # j se llama "indice"

print('--------------------------------------------------------------')

for k in range(1,10,2):     #tres parametros indican: inicio, fin y paso
    print(k)                # k se llama "indice"

print('--------------------------------------------------------------')
